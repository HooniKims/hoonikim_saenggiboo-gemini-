export function getClubHighSchoolQualityGuidance(schoolLevel) {
    if (schoolLevel !== "high") return "";

    return `[고등학교 동아리 특기사항 심화 작성 기준]
- 문제의식: 활동명 나열보다 학생이 무엇을 궁금해하고 왜 탐구를 시작했는지 드러냄
- 교과·전공 연결: 전공명을 직접 강조하기보다 생명과학, 공학, 교육, 경제·경영, 인문사회 등과 관련된 전공적 사고를 활동 과정에서 어떻게 사용했는지 서술함
- 구체적 역할: 주제 제안, 자료 조사, 실험 설계, 설문 문항 구성, 발표 구조 정리, 코딩·제작, 팀원 의견 조율, 결과 분석, 오류 원인 파악, 후속 탐구 제안 등 학생의 실제 역할을 드러냄
- 시행착오와 개선 과정: 예상과 다른 결과, 한계, 오류를 만났을 때 조건 확인, 변수 통제, 설계 수정, 관점 보완 등으로 문제를 해결하는 과정을 포함함
- 팀 활동 속 기여: 팀원 의견 정리, 역할 분담 조율, 자료 체계화, 활동 진행 보조 등 공동 활동 안에서의 기여를 구체적으로 서술함
- 후속 탐구와 성장: 탐구 이후 관련 개념 조사, 한계 인식, 개선 방향 제안, 교과 개념과의 연결 등 일회성 체험이 아닌 성장 과정으로 마무리함
- 핵심 흐름: 문제의식 → 교과·전공 개념 연결 → 구체적 탐구 과정 → 본인의 역할 → 난관과 수정 → 결과 또는 기여 → 후속 탐구와 성장`;
}

export function getBehaviorHighSchoolQualityGuidance(schoolLevel) {
    if (schoolLevel !== "high") return "";

    return `[고등학교 행동특성 및 종합의견 심화 작성 기준]
- 학교생활 전반: 특정 활동 하나가 아니라 학생이 학교생활 전체에서 어떤 사람인지 드러냄
- 지속적으로 관찰되는 태도: 꾸준한 수업 참여, 안정적인 출결 태도, 과제 수행 책임감, 맡은 역할의 지속적 수행, 차분한 생활 태도, 일관된 배려를 반복 행동으로 서술함
- 성실성과 책임감의 행동 근거: 성실하다는 평가어보다 수업 준비, 기한 내 역할 완수, 학습 루틴 유지, 학급 활동 수행 등 행동 장면으로 보여줌
- 배려와 관계 형성 방식: 친구 의견 경청, 소극적인 친구의 참여 도움, 역할 분담 배려, 갈등 상황 조율, 친구의 어려움을 살피는 모습을 구체화함
- 공동체에 미친 긍정적 영향: 학급 분위기 안정, 친구들의 신뢰 형성, 공동 활동 기여 등 공동체 안에서의 긍정적 영향을 드러냄
- 성장과 변화: 의견 표현 확대, 학습 습관 조정, 타인의 관점 이해, 질문과 복습 실천 등 앞으로의 성장 가능성을 행동 변화로 서술함
- 학생만의 이미지: 조용한 실무형, 따뜻한 조력자, 끈기 있는 노력형, 침착한 조율자, 활발한 분위기 메이커처럼 학생 고유의 결이 느껴지게 함
- 핵심 흐름: 지속적으로 관찰된 태도 → 구체적 학교생활 장면 → 관계 속 역할 → 공동체에 미친 영향 → 성장 가능성`;
}
